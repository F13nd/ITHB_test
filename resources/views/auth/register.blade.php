<script>
    function pilih_kota(id_provinsi) {
        $.ajax({
            url: '{{url('getkota').'/'}}' + id_provinsi,
            type: "GET",
            dataType: "JSON",
            success: function(data) {
                console.log(data);
                
                data.forEach(data => {    
                    $('#kota').append(`
                        <option value="`+data.id+`">`+data.nama_kota+`</option>
                    `);
                });
            }
        });
    }
</script>

